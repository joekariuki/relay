TEST_CASES: list[TestCase] = _build_test_cases()


def get_test_cases(
    category: EvalCategory | None = None,
    tags: set[str] | None = None,
    max_cases: int | None = None,
) -> list[TestCase]:
    """Filter test cases by category and/or tags."""
    results = TEST_CASES

    if category is not None:
        results = [tc for tc in results if tc.category == category]

    if tags is not None:
        results = [tc for tc in results if tags & set(tc.tags)]

    if max_cases is not None:
        results = results[:max_cases]

    return results
